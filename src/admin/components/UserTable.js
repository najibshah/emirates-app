export function UserTable({ data }) {
  const history = useNavigate();

  var rows = [];
  if (data) {
    data.map((dt) => {
      return rows.push(
        createData(dt.firstName, dt.lastName, dt.email, dt.role, dt.date)
      );
    });
  }

  return (
    <TableContainer component={Paper}>
      <h2 style={{ paddingLeft: "20px" }}>All Users</h2>
      <p style={{ paddingLeft: "20px" }}>
        Click on a row to manage the user's privileges
      </p>
      <Table sx={{ minWidth: 650 }} aria-label="simple table">
        <TableHead>
          <TableRow>
            <TableCell>First Name</TableCell>
            <TableCell align="right">Last Name</TableCell>
            <TableCell align="right">Email</TableCell>
            <TableCell align="right">Role</TableCell>
            <TableCell align="right">Date</TableCell>
          </TableRow>
        </TableHead>
        <TableBody>
          {rows.map((row, index) => (
            <TableRow
              key={index}
              sx={{ "&:last-child td, &:last-child th": { border: 0 } }}
              onClick={() => history(`/editRole/${row.email}`)}
            >
              <TableCell component="th" scope="row">
                {row.firstName}
              </TableCell>
              <TableCell align="right">{row.lastName}</TableCell>
              <TableCell align="right">{row.email}</TableCell>
              <TableCell align="right">{row.role}</TableCell>
              <TableCell align="right">{row.date}</TableCell>
            </TableRow>
          ))}
        </TableBody>
      </Table>
    </TableContainer>
  );
}
